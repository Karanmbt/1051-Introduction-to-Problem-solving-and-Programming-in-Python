import turtle as t

t.speed(0)
#Letter K for Karan
t.pensize(5)
t.penup()
t.setpos(-200,100)
t.right(90)
t.pendown()
t.fd(200)
t.setpos(-200,0)
t.left(140)
t.fd(120)
t.setpos(-200,0)
t.right(100)
t.fd(120)


#Letter T for Thumar
t.penup()
t.setpos(0,-100)
t.setheading(90)
t.pendown()
t.fd(200)
t.setheading(0)
t.fd(100)
t.setheading(180)
t.fd(200)



t.done()